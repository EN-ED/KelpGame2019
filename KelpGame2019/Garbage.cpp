#include "Garbage.hpp"



/// ---------------------------------------------------------------------------------------------------------------------------------------------------------
Garbage::Garbage(const int& t_x, const int& t_y)
{
	m_x = t_x;
	m_y = t_y;

	mD_garbage = LoadGraph("media\\garbage.png");
}



/// ---------------------------------------------------------------------------------------------------------------------------------------------------------
Garbage::~Garbage()
{
	if (mD_garbage != -1) DeleteGraph(mD_garbage);
}



/// ---------------------------------------------------------------------------------------------------------------------------------------------------------
void Garbage::Draw()
{
	DrawGraph(m_x, m_y, mD_garbage, true);
}



/// ---------------------------------------------------------------------------------------------------------------------------------------------------------
void Garbage::Process(const int& t_speed)
{
	m_x -= t_speed;
}



/// ---------------------------------------------------------------------------------------------------------------------------------------------------------
const int& Garbage::GetX() const
{
	return m_x;
}



/// ---------------------------------------------------------------------------------------------------------------------------------------------------------
const int& Garbage::GetY() const
{
	return m_y;
}
